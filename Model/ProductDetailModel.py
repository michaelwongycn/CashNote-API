class ProductDetail:
    def __init__(self, product_detail_id, product_id, product_purchase_price, stock, product_expired_date, product_detail_status):
        self.product_detail_id = product_detail_id
        self.product_id = product_id
        self.product_purchase_price = product_purchase_price
        self.stock = stock
        self.product_expired_date = product_expired_date
        self.product_detail_status = product_detail_status
